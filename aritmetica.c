#include<stdio.h>
#include "ojitos.h"
//SUMA
int suma(int a, int b){
  int s;
  s=a+b;
  return s;
}
//MULTI
int multiplicacion(int a, int b){
  int m;
  m=a*b;
  return m;
}
//RESTA
int resta(int a, int b){
  int r;
  r=a*b;
  return r;
}
//DIVISION
double division(double a, double b){
  double dv;
  dv=a/b;
  return dv;
}

